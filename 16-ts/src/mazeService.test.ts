import { expect, test } from 'vitest'
import { pt1, pt2 } from './mazeService'

test('Test input part 1', () => {
  const input = [
    '###############',
    '#.......#....E#',
    '#.#.###.#.###.#',
    '#.....#.#...#.#',
    '#.###.#####.#.#',
    '#.#.#.......#.#',
    '#.#.#####.###.#',
    '#...........#.#',
    '###.#.#####.#.#',
    '#...#.....#.#.#',
    '#.#.#.###.#.#.#',
    '#.....#...#.#.#',
    '#.###.#.#.#.#.#',
    '#S..#.....#...#',
    '###############',
  ]

  // 36 forward -- 7 turns
  expect(pt1(input)).toBe(7036)
})

